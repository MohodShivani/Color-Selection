document.getElementById('a').onclick=slateBlue
document.getElementById('b').onclick=darkGray
document.getElementById('c').onclick=goldenRod
document.getElementById('d').onclick=lightGrey

function slateBlue() {
    document.querySelector('body').style.backgroundColor='darkslateBlue'
}


function darkGray() {
    document.querySelector('body').style.backgroundColor='darkGray'
}

function goldenRod() {
    document.querySelector('body').style.backgroundColor='goldenRod'
}

function lightGrey() {
    document.querySelector('body').style.backgroundColor='lightGrey'
}